p = int(input("Введите значение п (количество элементов): "))

# 1. Создать список и добавить числа от 1 до п
my_list = list(range(1, p + 1))
print("Начальный список:", my_list)

# 2. Вставить число 11 на третье место (индекс 2)
if len(my_list) >= 3:
    my_list.insert(2, 11)
else:
    my_list.append(11)
print("После вставки 11 на третьем месте:", my_list)

# 3. Удалить число 3 из списка (если есть)
if 3 in my_list:
    my_list.remove(3)
print("После удаления числа 3:", my_list)

# 4. Добавить число 22 в начало списка
my_list.insert(0, 22)
print("После добавления 22 в начало:", my_list)

# 5. Найти индекс числа 11
if 11 in my_list:
    index_11 = my_list.index(11)
    print("Индекс числа 11:", index_11)
else:
    print("Число 11 не найдено в списке.")

# 6. Отсортировать список
my_list.sort()
print("Отсортированный список:", my_list)

# 7. Найти число 11 в отсортированном списке
try:
    index_11_sorted = my_list.index(11)
    print("Индекс числа 11 после сортировки:", index_11_sorted)
except ValueError:
    print("Число 11 не найдено после сортировки.")

# 8. Удалить второй и предпоследний элементы
if len(my_list) >= 2:
    del my_list[1]
else:
    print("Нет второго элемента для удаления.")

if len(my_list) >= 2:
    del my_list[-2]
else:
    print("Нет предпоследнего элемента для удаления.")

print("Результат после удаления второго и предпоследнего элементов:", my_list)

