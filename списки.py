# 1. Создаем список и добавляем элементы
my_list = []

# 3 целых числа
my_list.extend([1, 2, 3])

# 3 вещественных числа
my_list.extend([1.1, 2.2, 3.3])

# 4 строки символов
my_list.extend(["abc", "def", "ghi", "jkl"])

print("Начальный список:", my_list)

# 2. Проверка вхождения числа, введенного с клавиатуры
num = input("Введите число для проверки в списке: ")

try:
    num_int = int(num)
except ValueError:
    try:
        num_int = float(num)
    except ValueError:
        num_int = num

if num_int in my_list:
    print(f"Число {num_int} входит в список.")
else:
    print(f"Число {num_int} НЕ входит в список.")

# 3. Вставить число 20 на третье место (индекс 2)
my_list.insert(2, 20)
print("После вставки 20 на третье место:", my_list)

# 4. Удалить последний элемент
my_list.pop()
print("После удаления последнего элемента:", my_list)

# 5. Поменять местами первые два элемента
if len(my_list) >= 2:
    my_list[0], my_list[1] = my_list[1], my_list[0]
    print("После обмена первых двух элементов:", my_list)
else:
    print("Массив слишком короткий для обмена элементов.")

# 6. Создать новый список из последних трех элементов
if len(my_list) >= 3:
    last_three = my_list[-3:]
    print("Новый список из последних трех элементов:", last_three)
else:
    last_three = my_list[:]
    print("В списке менее 3 элементов, копируем весь список:", last_three)

# 7. Удалить первые три элемента из первого списка
del my_list[:3]
print("После удаления первых трех элементов:", my_list)

